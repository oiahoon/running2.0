type TranslateVars = Record<string, string | number>

type I18nContextValue = {
  locale: Locale
  dateLocale: string
  localeLabel: string
  setLocale: (locale: Locale) => void
  cycleLocale: () => void
  t: (key: string, vars?: TranslateVars) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

function interpolate(value: string, vars?: TranslateVars) {
  if (!vars) return value
  return Object.entries(vars).reduce((result, [key, replacement]) => {
    return result.replaceAll(`{${key}}`, String(replacement))
  }, value)
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>('en')

  useEffect(() => {
    const frame = window.requestAnimationFrame(() => {
      setLocaleState(detectLocale())
    })
    return () => window.cancelAnimationFrame(frame)
  }, [])

  const localeMeta = localeOptions.find((item) => item.locale === locale) || localeOptions[0]

  const setLocale = useCallback((nextLocale: Locale) => {
    setLocaleState(nextLocale)
    if (typeof window !== 'undefined') {
      window.localStorage.setItem(storageKey, nextLocale)
      const meta = localeOptions.find((item) => item.locale === nextLocale)
      document.documentElement.lang = meta?.htmlLang || nextLocale
    }
  }, [])

  useEffect(() => {
    document.documentElement.lang = localeMeta.htmlLang
  }, [localeMeta.htmlLang])

  const cycleLocale = useCallback(() => {
    const currentIndex = localeOrder.indexOf(locale)
    setLocale(localeOrder[(currentIndex + 1) % localeOrder.length])
  }, [locale, setLocale])

  const t = useCallback(
    (key: string, vars?: TranslateVars) => {
      const value = dictionaries[locale][key] || dictionaries.en[key] || key
      return interpolate(value, vars)
    },
    [locale]
  )

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      dateLocale: localeMeta.dateLocale,
      localeLabel: localeMeta.label,
      setLocale,
      cycleLocale,
      t,
    }),
    [cycleLocale, locale, localeMeta.dateLocale, localeMeta.label, setLocale, t]
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) throw new Error('useI18n must be used within I18nProvider')
  return context
}
